import { describe, it, expect } from 'vitest'
import { formatPhone } from './formatPhone'

describe('formatPhone', () => {
  it('возвращает пустую строку для пустого ввода', () => {
    expect(formatPhone('')).toBe('')
  })

  it('форматирует номер начинающийся с 8', () => {
    expect(formatPhone('89991234567')).toBe('+7 (999) 123-45-67')
  })

  it('форматирует номер начинающийся с 7', () => {
    expect(formatPhone('79991234567')).toBe('+7 (999) 123-45-67')
  })

  it('добавляет 7 если нет кода страны', () => {
    expect(formatPhone('9991234567')).toBe('+7 (999) 123-45-67')
  })

  it('игнорирует нецифровые символы', () => {
    expect(formatPhone('+7 (999) 123-45-67')).toBe('+7 (999) 123-45-67')
  })

  it('форматирует частично введённый номер — только код', () => {
    expect(formatPhone('7999')).toBe('+7 (999)')
  })

  it('форматирует частично введённый номер — до 7 цифр', () => {
    expect(formatPhone('7999123')).toBe('+7 (999) 123')
  })

  it('форматирует частично введённый номер — до 9 цифр', () => {
    expect(formatPhone('799912345')).toBe('+7 (999) 123-45')
  })

  it('обрезает лишние цифры до 11', () => {
    expect(formatPhone('799912345678')).toBe('+7 (999) 123-45-67')
  })
})
